package Scene;

public class They {
    private boolean nausea = false;
    private String name;

    public boolean isNausea() {
        return this.nausea;
    }

    public void setNausea(boolean nausea) {
        this.nausea = nausea;
    }

    public String getName() {
        return this.name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
